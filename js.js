// Initialize
function init() {
    setupSlider();
    setupEventListeners();
    updateLanguage();
    setupScrollAnimations();
    animateStats();
    if (window.requestIdleCallback) {
        requestIdleCallback(() => createParticles(), { timeout: 2000 });
    } else {
        setTimeout(createParticles, 2000);
    }
}

// Create animated particles (deferred)
function createParticles() {
    if (particlesLoaded) return;
    particlesLoaded = true;
    const particlesContainer = document.getElementById('particles');
    if (!particlesContainer) return;
    
    for (let i = 0; i < 12; i++) {
        const particle = document.createElement('div');
        particle.className = 'particle';
        particle.style.left = Math.random() * 100 + '%';
        particle.style.top = Math.random() * 100 + '%';
        particle.style.animationDelay = Math.random() * 20 + 's';
        particle.style.animationDuration = (15 + Math.random() * 10) + 's';
        particlesContainer.appendChild(particle);
    }
}

// Animate stats with counter
function animateStats() {
    const statNumbers = document.querySelectorAll('.stat-number');
    statNumbers.forEach(stat => {
        const text = stat.textContent;
        const match = text.match(/(\d+)/);
        if (match) {
            const target = parseInt(match[1]);
            let current = 0;
            const increment = Math.ceil(target / 30);
            const interval = setInterval(() => {
                current += increment;
                if (current >= target) {
                    current = target;
                    clearInterval(interval);
                }
                stat.textContent = text.replace(/\d+/, current);
            }, 50);
        }
    });
}

// Setup image slider
function setupSlider() {
    const sliderControls = document.getElementById('sliderControls');
    images.forEach((_, index) => {
        const btn = document.createElement('button');
        btn.className = `slider-btn ${index === 0 ? 'active' : ''}`;
        btn.addEventListener('click', () => goToImage(index));
        sliderControls.appendChild(btn);
    });
}

// Navigate slider
function goToImage(index) {
    currentImageIndex = index;
    document.getElementById('mainImage').src = images[index];
    updateSliderControls();
}

function nextImage() {
    currentImageIndex = (currentImageIndex + 1) % images.length;
    goToImage(currentImageIndex);
}

function prevImage() {
    currentImageIndex = (currentImageIndex - 1 + images.length) % images.length;
    goToImage(currentImageIndex);
}

function updateSliderControls() {
    document.querySelectorAll('.slider-btn').forEach((btn, index) => {
        btn.classList.toggle('active', index === currentImageIndex);
    });
}

// Auto-play slider
setInterval(nextImage, 4000);

// Setup event listeners
function setupEventListeners() {
    const safeAddEventListener = (element, event, handler) => {
        if (element && typeof handler === 'function') {
            element.addEventListener(event, handler);
        }
    };

    safeAddEventListener(document.getElementById('nextBtn'), 'click', nextImage);
    safeAddEventListener(document.getElementById('prevBtn'), 'click', prevImage);
    safeAddEventListener(document.getElementById('themeToggle'), 'click', toggleTheme);
    safeAddEventListener(document.getElementById('langToggle'), 'click', toggleLanguage);
    safeAddEventListener(document.getElementById('cartIcon'), 'click', openCart);
    safeAddEventListener(document.getElementById('cartCloseBtn'), 'click', closeCart);
    safeAddEventListener(document.getElementById('addToCartBtn'), 'click', buyNow);
    safeAddEventListener(document.getElementById('checkoutBtn'), 'click', openCheckout);
    safeAddEventListener(document.getElementById('checkoutCloseBtn'), 'click', closeCheckout);
    safeAddEventListener(document.getElementById('checkoutForm'), 'submit', submitOrder);

    safeAddEventListener(document.getElementById('heroCta'), 'click', () => {
        document.querySelector('.gallery-section').scrollIntoView({ behavior: 'smooth' });
    });

    document.addEventListener('click', (e) => {
        if (e.target.closest('.thumbnail')) {
            const thumbnails = Array.from(document.querySelectorAll('.thumbnail'));
            const index = thumbnails.indexOf(e.target.closest('.thumbnail'));
            if (index >= 0 && index < images.length) {
                goToImage(index);
            }
        }
    });

    // Payment selection
    document.querySelectorAll('.payment-card').forEach(btn => {
        btn.addEventListener('click', function() {
            document.querySelectorAll('.payment-card').forEach(b => b.classList.remove('selected'));
            this.classList.add('selected');
        });
    });
}

// Theme toggle
function toggleTheme() {
    isDarkMode = !isDarkMode;
    document.body.classList.toggle('dark-mode');
    document.getElementById('themeToggle').textContent = isDarkMode ? '☀️' : '🌙';
    localStorage.setItem('darkMode', isDarkMode);
}

// Language toggle
function toggleLanguage() {
    currentLanguage = currentLanguage === 'en' ? 'ar' : 'en';
    updateLanguage();
    localStorage.setItem('language', currentLanguage);
}

// Update language - Bilingual support
function updateLanguage() {
    const t = translations[currentLanguage];
    Object.keys(t).forEach(key => {
        const element = document.getElementById(key) || document.querySelector(`[id="${key}"]`);
        if (element) {
            const safeText = t[key].toString().replace(/[<>]/g, '');
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = safeText;
            } else {
                element.textContent = safeText;
            }
        }
    });

    const devCreditElement = document.querySelector('.dev-credit-small');
    if (devCreditElement) {
        devCreditElement.textContent = currentLanguage === 'ar' ? 'تم التطوير عبر شركة Step plus للبرمجيات' : 'Developed by Step Plus Software';
    }

    document.body.classList.toggle('rtl', currentLanguage === 'ar');
    document.body.classList.toggle('arabic-text', currentLanguage === 'ar');
    document.body.classList.toggle('english-text', currentLanguage === 'en');
    document.getElementById('langToggle').textContent = currentLanguage === 'en' ? 'AR' : 'EN';
}

// Buy Now - Bilingual "Buy Now" / "اشتري الآن", style Place Order like main buy btn
function buyNow() {
    const btn = document.getElementById('addToCartBtn');
    cart.push({ id: 1, name: 'Fixora GPS Tracker 403', price: 45, image: images[0] });
    updateCartBadge();
    btn.classList.add('added');
    setTimeout(() => {
        btn.classList.remove('added');
        openCheckout();
    }, 1500);
}

function updateCartBadge() {
    document.getElementById('cartBadge').textContent = cart.length;
}

function openCart() {
    document.getElementById('cartSidebar').classList.add('open');
    renderCart();
}

function closeCart() {
    document.getElementById('cartSidebar').classList.remove('open');
}

function renderCart() {
    const cartItems = document.getElementById('cartItems');
    if (cart.length === 0) {
        cartItems.innerHTML = `<div class="empty-cart">${translations[currentLanguage].emptyCart}</div>`;
        return;
    }

    cartItems.innerHTML = cart.map((item, index) => {
        const safeName = item.name.replace(/[<>]/g, '');
        const safeImage = item.image.replace(/[<>"']/g, '');
        const safePrice = item.price.toString().replace(/[^0-9.]/g, '');
        const qty = item.qty || 1;

        return `
        <div class="cart-item">
            <div class="cart-item-image">
                <img src="${safeImage}" alt="${safeName}" loading="lazy">
            </div>
            <div class="cart-item-details">
                <div class="cart-item-name">${safeName}</div>
                <div class="cart-item-price">$${safePrice}</div>
                <div class="quantity-control">
                    <button class="qty-btn" onclick="decreaseQty(${index})">−</button>
                    <span class="qty-display">${qty}</span>
                    <button class="qty-btn" onclick="increaseQty(${index})">+</button>
                </div>
                <button class="remove-btn" onclick="removeFromCart(${index})">${translations[currentLanguage].removeBtn}</button>
            </div>
        </div>
    `}).join('');

    const total = cart.reduce((sum, item) => sum + (parseFloat(item.price) * (item.qty || 1)), 0);
    document.getElementById('totalPrice').textContent = `$${total.toFixed(2)}`;
}

function increaseQty(index) {
    cart[index].qty = (cart[index].qty || 1) + 1;
    renderCart();
}

function decreaseQty(index) {
    if (cart[index].qty > 1) {
        cart[index].qty--;
    } else {
        removeFromCart(index);
    }
    renderCart();
}

function removeFromCart(index) {
    cart.splice(index, 1);
    updateCartBadge();
    renderCart();
}

function openCheckout() {
    if (cart.length === 0) return;
    document.getElementById('checkoutModal').classList.add('open');
}

function closeCheckout() {
    document.getElementById('checkoutModal').classList.remove('open');
}

function submitOrder(e) {
    e.preventDefault();

    const name = document.getElementById('fullName').value.trim();
    const phone = document.getElementById('phone').value.trim();
    const address = document.getElementById('deliveryAddress').value.trim();
    const selectedBtn = document.querySelector('.payment-card.selected');
    const payment = selectedBtn ? selectedBtn.dataset.method || selectedBtn.id.replace('payment', '') : 'cod';

    if (!name || !phone || !address || !payment) {
        alert('الرجاء ملء جميع الحقول واختيار وسيلة دفع');
        return;
    }

    if (!/^[0-9+\-\s()]+$/.test(phone)) {
        alert('رقم الهاتف يجب أن يحتوي على أرقام فقط');
        return;
    }

    const message = `${name} - ${phone} - ${address} - ${payment}`;
    window.open(`https://wa.me/963982721561?text=${encodeURIComponent(message)}`, '_blank');

    showSuccessModal();
    closeCheckout();
    closeCart();
    cart = [];
    updateCartBadge();
}

function showSuccessModal() {
    const modal = document.createElement('div');
    modal.className = 'modal-overlay open';
    modal.style.cssText = 'z-index: 1000 !important;';
    modal.innerHTML = `
        <div class="modal" style="max-width: 400px; text-align: center;">
            <div class="success-message">
                <div class="success-icon" style="font-size: 4rem; animation: bounce 1s infinite;">✅</div>
                <h2>تم ارسال طلبك بنجاح</h2>
                <p>سيقوم فريقنا بالتواصل معك في أقرب وقت ممكن</p>
                <button onclick="this.closest('.modal-overlay').remove()" class="submit-btn" style="margin-top: 1rem;">موافق</button>
            </div>
        </div>
    `;
    document.body.appendChild(modal);
    setTimeout(() => modal.remove(), 8000);
}

// Scroll animations
function setupScrollAnimations() {
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
            }
        });
    }, { threshold: 0.1 });

    document.querySelectorAll('.fade-in').forEach(el => observer.observe(el));
}

// Load saved preferences
function loadPreferences() {
    try {
        const darkMode = localStorage.getItem('darkMode');
        const language = localStorage.getItem('language');

        if (darkMode === 'true') {
            isDarkMode = true;
            document.body.classList.add('dark-mode');
            document.getElementById('themeToggle').textContent = '☀️';
        }

        if (language === 'en') {
            currentLanguage = 'en';
        }
    } catch (e) {
        console.warn('localStorage error:', e);
    }
}

// Initialize on load
loadPreferences();
init();
